    private void declineActionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_declineActionActionPerformed
        if(drugCaseRequest.getStatus().equals(ImplConst.CASE_NEW)){
            drugCaseRequest.setResolveDate(ImplCommonUtil.getTodaysDateBYDATE());
            drugCaseRequest.setStatus(ImplConst.CASE_DECLINED);
            drugCaseRequest.setReceiver(userAccount);
        } else {
            jLabel17.setText("Legal Action has already been taken against the drug. Legal Case Action Id is : " + drugCaseRequest.getWorkRequestId() + " Status " + drugCaseRequest.getStatus());
        }
    }//GEN-LAST:event_declineActionActionPerformed

    private void takeLegalActionButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_takeLegalActionButtonActionPerformed
        if(drugCaseRequest.getStatus().equals(ImplConst.CASE_NEW)){
            drugCaseRequest.setStatus(ImplConst.CASE_APPROVED);
            drugCaseRequest.setResolveDate(ImplCommonUtil.getTodaysDateBYDATE()); 
            Product product = drugCaseRequest.getProduct();
            ProductCase productCase = product.getProductCaseCatalog().addProductCase();
            productCase.setCaseId(drugCaseRequest.getWorkRequestId());
            productCase.setCaseDescription(drugCaseRequest.getDescription());
            productCase.setPerson(userAccount.getPerson());
            productCase.setDrugCaseRequest(drugCaseRequest);
            if (drugCaseRequest.getPriority().equals(ImplConst.PRIORITY_CRITICAL)){
                try {
                    SendNotificationMail.sendWarningMail(cityLawAgency,productCase);
                } catch (MessagingException ex) {
                    Logger.getLogger(ManageRequestAgainstMedicationJPanel.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            jLabel17.setText("Legal Action has been taken against the drug. Legal Case Action Id is : " + drugCaseRequest.getWorkRequestId() + " Status " + drugCaseRequest.getStatus());
        } else{
            jLabel17.setText("Legal Action has already been taken against the drug. Legal Case Action Id is : " + drugCaseRequest.getWorkRequestId() + " Status " + drugCaseRequest.getStatus());
        }
    }//GEN-LAST:event_takeLegalActionButtonActionPerformed


