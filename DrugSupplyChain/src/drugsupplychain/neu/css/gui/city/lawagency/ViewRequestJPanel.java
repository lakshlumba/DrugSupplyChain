    private void populateValues(){
       caseIDTextField.setText(drugCaseRequest.getWorkRequestId());
       priorityTextField.setText(drugCaseRequest.getPriority());
       requestDateTextField.setText(ImplCommonUtil.getFormattedDate(drugCaseRequest.getRequestDate()));
       if (null != drugCaseRequest.getResolveDate()){
            resolveDateTextField.setText(ImplCommonUtil.getFormattedDate(drugCaseRequest.getResolveDate()));
       }
       statusTextField.setText(drugCaseRequest.getStatus());
       personIDTextField.setText(String.valueOf(drugCaseRequest.getSender().getPerson().getPersonId()));
       personNameTextField.setText(drugCaseRequest.getSender().getPerson().getName());
       medicineIDTextField.setText(drugCaseRequest.getProduct().getBarcode());
       medicineNameTextField.setText(drugCaseRequest.getProduct().getProductName());
       manufactureIDTextField.setText(drugCaseRequest.getProduct().getManufacturerId());
       maufactureDateTextField.setText(drugCaseRequest.getProduct().getManufacturerName());
       expiryDateTextField.setText(ImplCommonUtil.getFormattedDate(drugCaseRequest.getProduct().getExpiryDate()));
       problemCausedTextField.setText(drugCaseRequest.getProblem());
       lincenseNumberTextField.setText(drugCaseRequest.getProduct().getMedicineLincense().getLicenseNumber());
    }
