    private void populateCaseDetails(){
        if(null == lawAgency){
            reportToHigherLevel.setEnabled(false);
        }
        caseIdTextField.setText(lawAgencyCaseRequest.getCaseId());
        statusTextField.setText(lawAgencyCaseRequest.getCaseStatus());
        senderTextField.setText(lawAgencyCaseRequest.getSender().getUsername());
        openDateTextField.setText(lawAgencyCaseRequest.getRequestDate().toString());
        if (lawAgencyCaseRequest.getResolveDate() != null){
            resolvedTextField.setText(lawAgencyCaseRequest.getResolveDate().toString());
        }
        priorityTextField.setText(String.valueOf(lawAgencyCaseRequest.getPriority()));
        caseTextArea.setText(lawAgencyCaseRequest.getCaseDescription().toString());
    }
