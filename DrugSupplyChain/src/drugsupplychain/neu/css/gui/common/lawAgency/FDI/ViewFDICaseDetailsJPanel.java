    private void backButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backButtonActionPerformed
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backButtonActionPerformed

    private void approveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approveButtonActionPerformed
        if (!lawAgencyCaseRequest.getCaseStatus().equals(ImplConst.CASE_APPROVED) && !nextLevel) {
            lawAgencyCaseRequest.setCaseStatus(ImplConst.CASE_APPROVED);
            lawAgencyCaseRequest.setResolveDate(ImplCommonUtil.getTodaysDateBYDATE());
            populateCaseDetails();
        }
    }//GEN-LAST:event_approveButtonActionPerformed

    private void reportToHigherLevelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reportToHigherLevelActionPerformed
        if (!(lawAgencyCaseRequest.getCaseStatus().equals(ImplConst.CASE_APPROVED))) {
            lawAgency.getWorkQueue().getWorkRequestList().add(lawAgencyCaseRequest);
            nextLevel = true;
            reportLabel.setText("Case has been reported to higher level");
        }               
    }//GEN-LAST:event_reportToHigherLevelActionPerformed


