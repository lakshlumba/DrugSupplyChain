    /**
     * populate the product table
     */
    private void populateProductTable(){
        ImplLogger.enterMethod();
        DefaultTableModel defaultTableModel = (DefaultTableModel)productTable.getModel();
        defaultTableModel.setRowCount(0);
        for (Product product : productCatalog.getProductList()){
            Object[] row = new Object[5];
            row[0] = product.getProductName();
            row[1] = product;
            row[2] = product.getManufactureDate().toString();
            row[3] = ImplCommonUtil.getFormattedDate(product.getExpiryDate());
            row[4] = product.getProductCaseCatalog().getProductCaseList().size();
            defaultTableModel.addRow(row);            
        }
        ImplLogger.exitMethod();
    }
