    /**
     * view medicine details button
     * @param evt 
     */
    private void viewMedicineButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewMedicineButtonActionPerformed
        ImplLogger.enterMethod();
        int selectedRow =  productTable.getSelectedRow();
        if (selectedRow < 0 ){
            JOptionPane.showMessageDialog(null, "Please select the row from the list");
        } else {
            Product product = (Product)productTable.getValueAt(selectedRow, 1);
            ViewMedicineDetails viewMedicineDetails =  new ViewMedicineDetails(userProcessContainer,product,organization);
            userProcessContainer.add("ViewMedicineDetails",viewMedicineDetails);
            CardLayout cardLayout = (CardLayout)userProcessContainer.getLayout();
            cardLayout.next(userProcessContainer);
        }  
        ImplLogger.exitMethod();
    }//GEN-LAST:event_viewMedicineButtonActionPerformed
