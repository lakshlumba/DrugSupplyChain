    /**
     * add new medicine
     * @param evt 
     */
    private void addNewMedicineButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addNewMedicineButtonActionPerformed
        ImplLogger.enterMethod();
        if (validateFields()){
            Product product = pharmaceauticalCompany.getProductCatalog().addProduct();
            product.setProductName(nameTextField.getText());
            product.setAvailable(Integer.parseInt(quantityTextField.getText()));
            product.setInitialQuantity(Integer.parseInt(quantityTextField.getText()));
            product.setChemicalComposition(new StringBuffer(chemicalTextArea.getText()));
            product.setExpiryDate(dateChooser.getSelectedDate().getTime());
            product.setManufacturerName(pharmaceauticalCompany.getName());
            Person person = pharmaceauticalCompany.getPersonDirectory().getPersonList().get(0);
            if (null != person){
                product.setManuFacturerEMAIL(person.getEmail());
            }
            product.setManufacturerId(String.valueOf(pharmaceauticalCompany.getOrganizationID()));
            product.setPrice(Integer.parseInt(priceTextField.getText()));
            if(Prescription.getSelection().equals(notRequiredRadioButton.getModel())){
               product.setIsPrescriptionRequired(false);
            } else {
                product.setIsPrescriptionRequired(true);
            }
            TrackOrganization trackOrganization = new TrackOrganization();
            trackOrganization.setOrganization(pharmaceauticalCompany);
            trackOrganization.setQuantity(Integer.parseInt(quantityTextField.getText()));
            trackOrganization.setPuchaseDate(ImplCommonUtil.getTodaysDate());
            product.getProductTracker().put(String.valueOf(pharmaceauticalCompany.getOrganizationID()), trackOrganization);
            product.setManufactureDate(ImplCommonUtil.getTodaysDateBYDATE());
            clearAll();
        }
        ImplLogger.exitMethod();
    }//GEN-LAST:event_addNewMedicineButtonActionPerformed

