     /**
     * validate the fields before submitting
     */
    private boolean validateFields(){
        ImplLogger.enterMethod();
        StringBuffer validationMessage = new StringBuffer();
        boolean isValidationPassed = true;
        if(ImplValidationUtils.isStringEmptyOrNull(nameTextField.getText())){
            validationMessage.append(ImplConst.VALIDATE_PRODUCT_NAME);
            validationMessage.append(ImplConst.NEXT_LINE);
            nameTextField.setBackground(Color.red);
            isValidationPassed = false;
        }
        if(ImplValidationUtils.isStringEmptyOrNull(chemicalTextArea.getText())){
            validationMessage.append(ImplConst.VALIDATE_PRODUCT_CHEMICAL_COMPOSITION);
            validationMessage.append(ImplConst.NEXT_LINE);
            chemicalTextArea.setBackground(Color.red);
            isValidationPassed = false;
        }
        if(!ImplValidationUtils.isInteger(quantityTextField.getText())){
            validationMessage.append(ImplConst.VALIDATE_PRODUCT_QUANTITY);
            validationMessage.append(ImplConst.NEXT_LINE);
            quantityTextField.setBackground(Color.red);
            isValidationPassed = false;
        }
        
        if(!ImplValidationUtils.isInteger(priceTextField.getText())){
            validationMessage.append(ImplConst.VALIDATE_PRODUCT_AVAILABLE);
            validationMessage.append(ImplConst.NEXT_LINE);
            priceTextField.setBackground(Color.red);
            isValidationPassed = false;
        }
        Calendar expiryDate = dateChooser.getSelectedDate();
        Calendar currentDate = Calendar.getInstance();
        if((expiryDate == null) || expiryDate.compareTo(currentDate) <= 0){
            validationMessage.append(ImplConst.VALIDATE_PRODUCT_EXPIRY_DATE);
            validationMessage.append(ImplConst.NEXT_LINE);
            dateChooser.setBackground(Color.red);
            isValidationPassed = false;
        }
        if(!isValidationPassed){
            JOptionPane.showMessageDialog(null,validationMessage);
        }
        ImplLogger.exitMethod();
        return isValidationPassed;
    }
